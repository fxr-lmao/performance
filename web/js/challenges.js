  _renderList() {
    const completed = DB.get('completed_challenges', []);
    const isFR = App._lang === 'fr';
    let html = '<div class="grid-2" style="gap:24px;align-items:start">';
    
    const renderCard = (c) => {
      const isDone = completed.includes(c.id);
      const title = isFR ? c.title_fr : c.title;
      const desc = isFR ? c.desc_fr : c.desc;
      const hazardHtml = c.hazard ? `<span class="hazard-badge">⚠️ ${isFR ? 'Risque Sécurité' : 'Safety Hazard'}</span>` : '';
      return `
        <div class="card" style="opacity:${isDone ? 0.6 : 1}; border-color:${isDone ? 'var(--green)' : c.hazard ? 'rgba(239, 68, 68, 0.4)' : 'var(--border)'}">
          <div style="display:flex;justify-content:space-between;align-items:flex-start">
            <div class="card-title" style="${isDone ? 'text-decoration:line-through' : ''}">
              ${title} ${hazardHtml}
            </div>
            <div style="font-weight:900;color:var(--accent);font-size:14px">+${c.xp} XP</div>
          </div>
          <div style="font-size:13px;color:var(--text2);margin:12px 0;line-height:1.5">${desc}</div>
          ${isDone 
            ? `<div style="color:var(--green);font-weight:700;font-size:13px;text-align:center">${App.t('challenge_complete')}</div>` 
            : `<button class="btn btn-ghost btn-full" onclick="Challenges.completeChallenge('${c.id}', ${c.xp}, '${title.replace(/'/g, "\\'")}')">${isFR ? 'Marquer Complété' : 'Mark Completed'}</button>`
          }
        </div>`;
    };

    const renderTier = (tierNum, title, color) => {
      const tierChallenges = this.CHALLENGE_LIST.filter(c => c.tier === tierNum);
      const isExpanded = this._expandedTiers[tierNum];
      const first5 = tierChallenges.slice(0, 5);
      const rest = tierChallenges.slice(5);
      
      return `
      <div style="display:flex;flex-direction:column;margin-bottom:16px">
        <div style="font-size:18px;font-weight:800;color:${color};margin-bottom:16px;border-bottom:1px solid ${color};padding-bottom:8px">
          ${title} <span style="font-size:12px;color:var(--text3);margin-left:8px">(${tierChallenges.length})</span>
        </div>
        <div style="display:flex;flex-direction:column;gap:16px;margin-bottom:16px">
          ${first5.map(renderCard).join('')}
        </div>
        <div class="accordion-content ${isExpanded ? 'expanded' : ''}" id="tier-${tierNum}-content">
          <div class="accordion-inner" style="display:flex;flex-direction:column;gap:16px;padding-bottom:16px">
            ${rest.map(renderCard).join('')}
          </div>
        </div>
        <button id="tier-${tierNum}-btn" class="btn btn-ghost btn-full" onclick="Challenges.toggleTier(${tierNum})">
          ${isExpanded ? (isFR ? 'Réduire ▲' : 'Show Less ▲') : (isFR ? 'Voir Plus ▼' : 'Show More ▼')}
        </button>
      </div>`;
    };

    html += renderTier(1, isFR ? 'Palier I : Inconfort' : 'Tier I: Discomfort', 'var(--text)');
    html += renderTier(2, isFR ? 'Palier II : Endurance' : 'Tier II: Endurance', 'var(--orange)');
    html += '</div><div class="grid-2" style="gap:24px;margin-top:24px">';
    html += renderTier(3, isFR ? 'Palier III : Le Creuset' : 'Tier III: The Crucible', 'var(--red)');
    html += '</div>';

    document.getElementById('challenges-container').innerHTML = html;
  }
